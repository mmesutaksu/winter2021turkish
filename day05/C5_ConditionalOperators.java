package day05;

public class C5_ConditionalOperators {

	public static void main(String[] args) {
		// AND && , OR ||
		
		int sayi4=4;
		
		boolean isTrue= 5>4 && 7-3>0 && 7-3==sayi4;
		
		System.out.println(isTrue);
		
		int x=10;
		int y=5;
		
		System.out.println(x/y==2 && x*y>20 && x-y>0);   // true
		
		System.out.println(x+y<0 && x-y>0);     // true
		
		// OR operatöründe 1 dogru bile olsa true dur.
		
		System.out.println(x<y || x+y<0 || x*y>0);  // x*y>0 dogru oldugundan true.
		
		System.out.println(x<y && x+y<0 && x*y>0);  // bir yanlis bile olsa sonuc false.
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
