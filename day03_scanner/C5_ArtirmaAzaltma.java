package day03_scanner;

public class C5_ArtirmaAzaltma {
	
	public static void main(String[] args) {
		
		// arttirmak=incremet
		int num=15;
		int num2=num+2;
		System.out.println(num2+num);
		
		num=num+5;
		System.out.println(num);
		
		num=num+8; //num=28
				
		num +=10;   //num=38 num +=5 --> num=num+5
		
		num+=1;     //num=39 sadeece 1 arttirmak icin num++; yeterlidir
		
		num++;      //num=40
		
				
		System.out.println(num);
		
		
		
		
		
		
	}
	
	

}
