package day03_scanner;

public class C4_DataCastingDaralma {

	public static void main(String[] args) {
		
		// double bir degisken olusturup önce int e sonra byte a cevirin
		
		double numDouble=129;
		
		int numInt=(int)numDouble;
		System.out.println("int degisken degeri : "+numInt);
		
		byte numByte=(byte)numInt;
		System.out.println("byte degisken degeri: "+numByte);
		/* daha dar (narrow) bir data tipine cevirirken data tipinin sinirindan buyukse
		en kücük negatif sinirindan saymaya devam eder.*/
		
		
		
		
		
		
		
		
		
		
		
	}

}
