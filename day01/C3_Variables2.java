package day01;

public class C3_Variables2 {

	public static void main(String[] args) {

		String isim = "Mesut";
		String soyisim = "Aksu";

		System.out.print(isim);
		System.out.println(soyisim);

		System.out.println(isim + ' ' + soyisim);
		System.out.println(isim + " " + soyisim);

		char bosluk = ' ';
		System.out.println(isim + bosluk + soyisim);

	}

}
