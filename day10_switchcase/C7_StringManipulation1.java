package day10_switchcase;

public class C7_StringManipulation1 {

	public static void main(String[] args) {
		// String non-primitiv data türüdür.
		// non-primitiv data türleri value ile birlikte methodlara sahiptirler
		//methodlari kullanarak Stringleri degisirmeye String Manipulation denir.
		
		
		String str="Hello World";
		
		System.out.println(str.toUpperCase()); // HELLO WORLD
		
		System.out.println(str); //Hello World
		
		String str2=str.toUpperCase();
		
		System.out.println(str); //Hello World
		
		System.out.println(str2); //HELLO WORLD
		
		
		
		

	}

}
